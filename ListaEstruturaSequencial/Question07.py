# 7. Crie um algoritmo que leia dois números e mostre sua soma, subtração e divisão. 

num1 = int(input('Digite um numero '))
num2 = int(input('Digite um segundo numero '))

soma = num1 + num2
subtração = num1 - num2
divisão = num1 / num2

print(f'Soma {soma}')
print(f'subtração {subtração}')
print(f'divisão {divisão}')